    public static SendPacketOpcode getByValue(int type) {
        for (SendPacketOpcode l : SendPacketOpcode.values()) {
            if (l.getValue() == type) {
                return l;
            }
        }
        return UNKNOWN;
    }

    public static Properties getDefaultProperties() throws Exception {
        String fileName = "sendops.properties";
        Properties props = new Properties();
        try (FileInputStream fis = new FileInputStream(fileName);InputStreamReader isr = new InputStreamReader(fis, StringUtil.codeString(fileName));BufferedReader buff = new BufferedReader(isr)) {
            props.load(buff);
        } catch (Exception ex) {
            File file = new File("properties/" + fileName);
            if (file.exists() && file.isFile()) {
                FileInputStream fis = new FileInputStream(file);
                InputStreamReader isr = new InputStreamReader(fis, StringUtil.codeString(file.getPath()));
                BufferedReader buff = new BufferedReader(isr);
                props.load(buff);
                fis.close();
                isr.close();
                buff.close();
            }
        }
        return props;
    }

    public static void reloadValues() {
        try {
            ExternalCodeTableGetter.populateValues(getDefaultProperties(), values());
        } catch (Exception e) {
            throw new RuntimeException("加載 sendops.properties 文件出現錯誤", e);
        }
    }

